    let short_list_len = 5;
    let medium_list_len = 20;
    let long_list_len = 60;

    use tokio::runtime::Builder;
    let runtime = Builder::new_multi_thread().enable_all().build().unwrap();

    fn create_context(
        array_len: usize,
        batch_size: usize,
    ) -> Result<ExecutionContext> {
        let schema = Arc::new(Schema::new(vec![
            Field::new("utf8short", DataType::Utf8, false),
            Field::new("utf8long", DataType::LargeUtf8, false),
            Field::new("utf8shortNullable", DataType::Utf8, true),
            Field::new("utf8longNullable", DataType::LargeUtf8, true),
        ]));

        // define data.
        let batches = (0..array_len / batch_size)
            .map(|_i| {
                let short_data = create_str_data_no_null(
                    batch_size,
                );
                let long_data = create_str_data_no_null(
                    batch_size,
                );
                let short_nullable_data =
                    create_str_data(batch_size, 0.8);
                let long_nullable_data =
                    create_str_data(batch_size, 0.8);

                RecordBatch::try_new(
                    schema.clone(),
                    vec![
                        Arc::new(StringArray::from(short_data)),
                        Arc::new(LargeStringArray::from(long_data)),
                        Arc::new(StringArray::from(short_nullable_data)),
                        Arc::new(LargeStringArray::from(long_nullable_data)),
                    ],
                )
                .unwrap()
            })
            .collect::<Vec<_>>();

        let mut ctx = ExecutionContext::new();

        // declare a table in memory. In spark API, this corresponds to createDataFrame(...).
        let provider = MemTable::try_new(schema, vec![batches])?;
        ctx.register_table("t", Arc::new(provider))?;

        Ok(ctx)
    }

